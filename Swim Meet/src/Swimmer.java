
public class Swimmer {


	    String Name;
	    int Prep;
	    int Food;

	    public Swimmer (String n, int p, int f)
	        {
	        Name = n;
	        Prep = p;
	        Food = f;
	        }

	    public String getName()
	        {
	        return Name;
	        }

	    public void setName(String Name)
	        {
	        this.Name = Name;
	        }

	    public int getPrep()
	        {
	        return Prep;
	        }

	    public void setPrep(int Prep)
	        {
	        this.Prep = Prep;
	        }
	    public int getFood() {
	    	return Food;
	    }
	    public void setFood(int Food)
	    {
	    	this.Food = Food;
	    }
	    }

	


